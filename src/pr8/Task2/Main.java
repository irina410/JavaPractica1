package pr8.Task2;

public class Main {
    public static void main(String[] args) {
        // Задача 2: От 1 до n
        int n2 = 10;  // Задаем значение n
        System.out.println("Задача 2: От 1 до n");
        for (int i = 1; i <= n2; i++) {
            System.out.print(i + " ");  // Выводим числа от 1 до n
        }
        System.out.println("\n");
    }
}
